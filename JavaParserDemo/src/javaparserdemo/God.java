public class God {
    private int Age;
    private int Voiture;
    private int Rail;
    private int Poutrelle;
    private int Lanterne;

    /**
     * @return the Age
     */
    public int getAge() {
        return Age;
    }

    /**
     * @param Age the Age to set
     */
    public void setAge(int Age) {
        this.Age = Age;
    }

    /**
     * @return the Voiture
     */
    public int getVoiture() {
        return Voiture;
    }

    /**
     * @param Voiture the Voiture to set
     */
    public void setVoiture(int Voiture) {
        this.Voiture = Voiture;
    }

    /**
     * @return the Rail
     */
    public int getRail() {
        return Rail;
    }

    /**
     * @param Rail the Rail to set
     */
    public void setRail(int Rail) {
        this.Rail = Rail;
    }

    /**
     * @return the Poutrelle
     */
    public int getPoutrelle() {
        return Poutrelle;
    }

    /**
     * @param Poutrelle the Poutrelle to set
     */
    public void setPoutrelle(int Poutrelle) {
        this.Poutrelle = Poutrelle;
    }

    /**
     * @return the Lanterne
     */
    public int getLanterne() {
        return Lanterne;
    }

    /**
     * @param Lanterne the Lanterne to set
     */
    public void setLanterne(int Lanterne) {
        this.Lanterne = Lanterne;
    }
    
    
    
    
}
